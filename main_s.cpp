#include "server.hpp"

void handler(int);
int main(int, char**);

static SERVER* srv;

int main(int argc, char* args[])
{
	srv = new SERVER();

	signal(SIGABRT, handler);
	signal(SIGINT, handler);
	signal(SIGTERM, handler);
	signal(SIGABRT, handler);

	if (!srv->start()) cout << "FAIL\n";
	else while (srv->loop());

	delete srv;

	return 0;
}

void handler(int signal)
{
	srv->end(signal);
}
